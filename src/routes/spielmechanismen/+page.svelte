<h2 class="h2 mt-6">Misserfolg</h2>
<ul>
	<li>Weniger Erfolge, als durch die Schwierigkeit vorgegeben</li>
	<li>Patzer: Misserfolg und mehr 1en als Erfolge</li>
</ul>

<h2 class="h2 mt-6">Wiederstreit</h2>
<ul>
	<li>Gegner würfeln: Wer mehr Erfolge erzielt, gewinnt</li>
	<li>Bei Gleichstand entscheiden die Trigger</li>
	<li>Im Wettkampf: Unentschieden ist möglich</li>
	<li>Ja/Nein: gewinnt der Agierende nicht, verliert er</li>
	<li>Widerstreit ist möglich zwischen verschiedenen Fertigkeiten</li>
</ul>

<h2 class="h2 mt-6">Kombinationen</h2>
<p>
	Nicht jede Aktion lässt sich mit genau einer Fertigkeit und einem Aktionswurf abhandeln. Manchmal
	beschreibt erst das Zusammenspiel von zwei Fertigkeiten den gewünschten Effekt. Hier kommen die
	Kombinationen ins Spiel:
</p>

<ul>
	<li>Zwei Aktionen würfeln</li>
	<li>Nur wenn die erste erfolgreich ist, darf die zweite angegangen werden</li>
	<li>Trigger aus erster Aktion werden in die zweite Aktion übertragen</li>
</ul>

<h2 class="h2 mt-6">Aufwand</h2>
<p>
	Manche Herausforderung setzt sich aus vielen kleinen Aktionen zusammen. Beispielsweise muss ein
	komplexer Sicherungsmechanismus an einer Tür geknackt werden, während Gegner heranstürmen. Um die
	Spannung zu steigern, kann der Spielleiter befinden, dass eine Mindestzahl an Gesamterfolgen
	erbracht werden muss, über mehrere Würfe hinweg. Trigger würden als Zusatzerfolge zählen. Erst
	wenn ausreichend Erfolge erwürfelt wurden, gilt die Aktion als bestanden - und die Tür schwingt
	auf. Das lässt sich sowohl mit einem Widerstreit und/oder Kombinationen verknüpfen.
</p>

<ul>
	<li>Mehrere erfolgreiche Aktionen sind erforderlich</li>
	<li>Erst wenn eine Zahl an Gesamterfolgen erreicht wurde, gilt die Aktion als geglückt</li>
	<li>Trigger zählen als Zusatzerfolg</li>
</ul>

<h2 class="h2 mt-6">Kooperation</h2>
<p>
	Charaktere treten als Gruppe auf, und als Gruppe können sie sich untereinander aushelfen. So
	können aufwändige Handlungen gemeinsam angegangen werden - sofern die Situation dies zulässt: Eine
	Tür bricht schneller aus der Verankerung, wenn sich mehrere Schultern dagegen stemmen. Die Spieler
	legen ihre Erfolge und Trigger zusammen und treten als Einheit auf. Es ist allerdings nicht
	hilfreich, dem Fahrer des Wüstenbuggies ins Lenkrad zu greifen. Hier werden keine Erfolge addiert.
</p>

<ul>
	<li>
		Kooperative aufwändige Aktionen: Situationsabhängig können Erfolge und Trigger mehrerer
		Charaktere addiert werden
	</li>
	<li>
		Kooperative Aktionswürfe: Unterstützer würfelt mit Schwierigkeit +1, Trigger werden zu
		Bonuswürfeln für den Unterstützten
	</li>
</ul>
