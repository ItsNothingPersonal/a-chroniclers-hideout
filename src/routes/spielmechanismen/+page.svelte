<h1 class="h1">Spielmechanismen</h1>
<p>
	Hier werden die grundsätzlichen Mechanismen erklärt. Welche Attribute und sonstige Werte gibt es?
	Wie werden Proben gewürfelt? Und warum kann ich nicht Wille & Glaube bzw. Fokus & Primal
	gleichzeitig wählen?
</p>
<h2 class="h2 mt-6">Attribute</h2>
<p>
	In jedem Lebewesen bestimmen sechs Attribute die körperlichen Grundzüge. Sie sind am ehesten als
	genetische Veranlagung zu verstehen - im Laufe eines Lebens können und werden sich Attribute
	verändern, aber langsamer als die erlernten Fertigkeiten. Die sechs Attribute sind Körper,
	Geschicklichkeit, Charisma, Verstand, Psyche und Instinkt. In ihrer Bedeutung sind sie
	gleichwertig - abhängig davon, wie der Charakter auftreten soll und was seine Stärken und
	Schwächen sind.
</p>
<ul>
	<li>
		<b>Körper (KÖR):</b> Steht für brachiale Gewalt, für Zähigkeit und Ausdauer. Menschen mit hohem Körperattribut
		sinken später in den Staub und stehen früher wieder auf. Allein durch ihre Physis beeindrucken sie
		ihre Gegner. Wer seinen Körper in Form hält, wird mit Schultern belohnt, auf die viel geladen werden
		kann. Das Attribut lässt einen Menschen länger überleben, ihn Schmerzen widerstehen und seinen Körper
		als Waffe einsetzen.
	</li>
	<li>
		<b>Geschichlichkeit (GES):</b> Kraft ist nichts ohne Kontrolle. Menschen mit ausgeprägter Geschicklichkeit
		sind beweglicher, können Gefahren ausweichen, manipulieren komplexe Mechanismen und erledigen ihre
		Gegner auf Distanz mit einer Schusswaffe. Vielleicht mit einer Flinte, an der sie den Lauf ausgefeilt
		und die Mechanik neu eingestellt haben.
	</li>
	<li>
		<b>Charisma (CHA):</b> Menschen mit Charisma bestechen durch authentische Präsenz; sie müssen sich
		nicht verstellen, um gemocht oder verehrt zu werden. Andere folgen ihnen aus Überzeugung und mit
		Hingabe. Charisma öffnet die Pforten der Welt: Diplomatie, Verhandlung und Einigkeit erfordern Menschen
		mit Strahlkraft, die sich von der Masse abheben und ihresgleichen zu führen vermögen.
	</li>
	<li>
		<b>Verstand (VER):</b> Ein trainierter und ausgeprägter Verstand begünstigt abstraktes Denken und
		tiefste Konzentration. Beides ist essentiell, wenn Technik oder Wissenschaft verstanden und eingesetzt
		werden müssen. Wissen aus verschiedensten Quellen wird von einem schlauen Kopf schnell aufgenommen
		und ins Denken einbezogen. Verstand bedeutet Planung und Voraussicht, bedachtes Handeln mit einem
		geschärften Blick für die Konsequenzen.
	</li>
	<li>
		<b>Psyche (PSY):</b> Psyche ist das Feuer, das unser Innenleben auf Temperatur hält, das unseren
		Willen oder Glauben antreibt. Psyche ist die Stärke des Geistes, die ungeliebte Schwester des Verstands.
		Aus ihr speisen sich Lüge und Trug, Einschüchterung und mentale Überlegenheit. Mit der Kraft der
		Psyche lassen sich die Einflüsse des Primers im Zaum halten und die Grenzen der eigenen Leistungsfähigkeit
		überwinden.
	</li>
	<li>
		<b>Instinkt (INS):</b> Unser Instinkt hat uns die Zeit überdauern lassen, als weder Sprache noch
		Regeln uns führten, und wir trotzdem zusammenhalten mussten. Instinkt steuert unsere rudimentären
		Reaktionen auf äußere Einflüsse, lässt das Tier in uns von der Leine. In einer Welt, die jedes Maß
		verloren hat, sichert Instinkt das Überleben.
	</li>
</ul>
<p>
	Hinweis zur Schreibweise: Wann immer in den Büchern Bezug auf eine Fertigkeit genommen wird, wird
	auch das Attribut als Kürzel vorangestellt. „KÖR+Kraft“ bezieht sich beispielsweise auf die
	Fertigkeit Kraft, die unter dem Attribut Körper (KÖR) zu finden ist.
</p>
<p>
	Das „+“ bedeutet, dass Attribut- und Fertigkeitenwert addiert werden und somit der Aktionswert
	gefragt ist - was es damit auf sich hat, erfahrt ihr später.
</p>

<h2 class="h2 mt-6">Fähigkeiten</h2>
<p>
	Jedem Attribut stehen genau sechs Fertigkeiten zur Seite. Attribut und Fertigkeit sind voneinander
	abhängig und stützen sich: Die Fertigkeit Wissenschaft gedeiht nur auf dem Attribut Verstand; die
	Fertigkeit Nahkampf baut auf dem Attribut Körper auf; Fingerfertigkeit ist nichts ohne
	Geschicklichkeit. Einige Fertigkeiten werden von Kämpfern benötigt, andere von Händlern oder von
	Trickbetrügern.
</p>
<h3 class="h3 mt-4">Aktion</h3>
<p>
	Jede Fertigkeit hat ihre Einsatzgebiete. In den Beschreibungen sind einige davon beispielhaft
	unter „Aktion“ aufgeführt. Diese Handlungen werden aktiv vom Spieler angekündigt; sie beanspruchen
	meist etwas Zeit oder zumindest ein Bewusstseinsmoment.
</p>
<h3 class="h3 mt-4">Reaktion</h3>
<p>
	Die unter „Reaktion“ geführten Anwendungen werden durch äußere Umstände erzwungen oder angeboten.
	Beispielsweise kann der Spielleiter seiner Gruppe sagen: „Die Nacht ist pechschwarz. Würfelt, ob
	ihr etwas bemerkt.“ Die Spieler würfeln auf Wahrnehmung - und wenn sich dort draußen wirklich ein
	Shutter heranschleicht, hören sie ihn mit einem geglückten Wurf vielleicht.
</p>
<h3 class="h3 mt-4">Kombination</h3>
<p>
	Nicht jede Handlung lässt sich auf genau eine Fertigkeit ableiten. Manchmal ist es die Kombination
	von zwei Fertigkeiten, die den gewünschten Effekt mit sich bringt: Ein Richter lässt sich durch
	distinguierte Wortwahl (Charisma+Ausdruck) leichter täuschen (Psyche+Täuschung); ein Angriff aus
	dem Hinterhalt (Geschicklichkeit+Heimlichkeit, gefolgt von Körper+Nahkampf) wird zu einer
	Meuchelattacke. Die Regeln für Kombinationen folgen später in diesem Kapitel.
</p>
<h3 class="h3 mt-4">Besonderheit</h3>
<p>
	Einige Fertigkeiten warten mit einer „Besonderheit“ auf: Aus ihnen werden weitere Spielwerte
	errechnet, oder sie unterliegen Beschränkungen. Im Kapitel „Charaktererschaffung“ wird darauf
	konkret eingegangen, hier werden die Besonderheiten nur für das spätere Nachschlagen aufgeführt.
</p>

<h2 class="h2 mt-6">Der Aktionswert</h2>
<p>
	Natürliche Begabung und jahrelanges Training ergänzen sich im KatharSys zu einem Wert: Dem
	Aktionswert, abgekürzt AW. Er bestimmt die Chance eines Charakters, in einer schwierigen Situation
	mit einer Handlung erfolgreich zu sein.
</p>
<h3 class="h3 mt-4">AKTIONSWERT (AW) = ATTRIBUT + FERTIGKEIT</h3>
<p>
	Sobald eine Handlung die Wahrscheinlichkeit des Scheiterns in sich trägt, kommen die Würfel ins
	Spiel: Der Spieler entscheidet sich für ein Vorgehen und eine dazu passende Fertigkeit, bestimmt
	den Aktionswert und würfelt mit ebenso vielen sechsseitigen Würfeln. Jetzt zählt er noch die
	Würfel, die eine 4, 5 oder 6 zeigen (sie werden „Erfolge“ genannt) und nennt die Zahl laut. Fällt
	ein Aktionswert größer als 12 aus, werden alle überzähligen Würfel über der 12 als automatische
	Erfolge gewertet und der Spieler würfelt mit 12 Würfeln. Unter keinen Umständen sind mehr als 12
	Würfel möglich.
</p>
<p>Aktion gelingt, wenn Zahl der Erfolge größer oder gleich Höhe der Schwierigkeit</p>
<p>
	Wenn in einem Buchabenteuer von Charakteren ein Aktionswurf erwartet wird, steht die Schwierigkeit
	in Klammern hinter der Fertigkeit, also beispielsweise INS+Zähmen(2).
</p>

<p>
	Boni und Mali werden im KatharSys immer in Würfeln angegeben. So steht ein +2W für einen Bonus in
	Höhe von zwei Würfeln, und ein -3W für einen Malus von drei Würfeln.
</p>
